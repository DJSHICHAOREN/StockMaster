package com.example.stockmaster.entity.sina;

public class TodaySinaStockPrice {
    private String m;
    private String p;
    private String v;
    private String avg_p;

    public String getM() {
        return m;
    }

    public void setM(String m) {
        this.m = m;
    }

    public String getP() {
        return p;
    }

    public void setP(String p) {
        this.p = p;
    }

    public String getV() {
        return v;
    }

    public void setV(String v) {
        this.v = v;
    }

    public String getAvg_p() {
        return avg_p;
    }

    public void setAvg_p(String avg_p) {
        this.avg_p = avg_p;
    }
}
